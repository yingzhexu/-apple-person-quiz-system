function shuffle(items) {
  return [...items].sort(() => Math.random() - 0.5);
}

function randomizeOptions(item) {
  const original = Object.entries(item.options);
  const correctText = item.options[item.answer];
  const labels = ["A", "B", "C", "D"];
  const mixed = shuffle(original);
  const options = {};
  let answer = item.answer;
  mixed.forEach(([, value], index) => {
    const label = labels[index];
    options[label] = value;
    if (value === correctText) answer = label;
  });
  const optionMap = {};
  mixed.forEach(([oldLabel], index) => {
    optionMap[labels[index]] = oldLabel;
  });
  return { ...item, options, answer, optionMap };
}

function sameOptionOrder(source, current) {
  if (!current) return randomizeOptions(source);
  const options = {};
  let answer = source.answer;
  Object.keys(current.options).forEach((label) => {
    const oldLabel = current.optionMap?.[label] || label;
    const newValue = source.options[oldLabel];
    options[label] = newValue;
    if (oldLabel === source.answer) answer = label;
  });
  return { ...source, options, answer, optionMap: current.optionMap };
}

