function renderQuestion() {
  clearAutoNext();
  const item = state.round[state.index];
  state.locked = false;
  nextBtn.disabled = true;
  nextBtn.textContent = t("next");
  progressText.textContent = t("questionCounter", state.index + 1, state.roundSize);
  scoreText.textContent = `${t("score")} ${state.score}`;
  streakText.textContent = t("streak", state.streak);
  meterFill.style.width = `${(state.index / state.roundSize) * 100}%`;
  questionText.textContent = item.question;
  optionsEl.innerHTML = "";
  document.querySelector(".question-card").classList.remove("question-enter");
  void document.querySelector(".question-card").offsetWidth;
  document.querySelector(".question-card").classList.add("question-enter");

  Object.entries(item.options).forEach(([key, value]) => {
    const button = document.createElement("button");
    button.className = "option";
    button.innerHTML = `<span class="label">${key}</span><span>${value}</span>`;
    button.addEventListener("click", () => answer(key, button));
    optionsEl.appendChild(button);
  });
}

function answer(choice, button) {
  if (state.locked) return;
  state.locked = true;
  button.classList.add("selected");
  const item = state.round[state.index];
  const isCorrect = choice === item.answer;
  if (isCorrect) {
    state.score += 1;
    state.streak += 1;
    state.bestStreak = Math.max(state.bestStreak, state.streak);
  } else {
    state.streak = 0;
  }

  [...optionsEl.children].forEach((option) => {
    option.disabled = true;
    const label = option.querySelector(".label").textContent;
    if (label === item.answer) option.classList.add("correct");
  });
  if (!isCorrect) button.classList.add("wrong");
  if (isCorrect) showSuccessAnimation();

  scoreText.textContent = `${t("score")} ${state.score}`;
  streakText.textContent = t("streak", state.streak);
  showAchievementToast(Math.round((state.score / state.roundSize) * 100));
  tone(isCorrect ? "correct" : "wrong");
  const pool = isCorrect ? feedbackLines[state.lang].correct : feedbackLines[state.lang].wrong;
  const prefix = pool[Math.floor(Math.random() * pool.length)];
  speak(t("correctAnswer", prefix, item.answer));
  nextBtn.disabled = false;
  nextBtn.textContent = t("skip");
  state.autoNextTimer = window.setTimeout(nextQuestion, isCorrect ? 1350 : 1650);
}

function nextQuestion() {
  clearAutoNext();
  state.index += 1;
  if (state.index >= state.roundSize) {
    showResult();
    return;
  }
  renderQuestion();
}

function showResult() {
  quizView.classList.add("hidden");
  finalIntro.classList.add("hidden");
  resultView.classList.remove("hidden");
  meterFill.style.width = "100%";
  if (state.perfectMode) {
    state.score = state.roundSize;
    state.bestStreak = state.roundSize;
  }
  const rate = Math.round((state.score / state.roundSize) * 100);
  const band = achievementForRate(rate);
  const achievement = pickAchievement(band, state.score || 1);
  $("finalScore").textContent = `${state.score} / ${state.roundSize}`;
  $("finalRate").textContent = `${rate}%`;
  const copy = resultCopy(rate);
  $("resultTitle").textContent = copy.title;
  achievementCard.innerHTML = `<span>${band.level}</span><strong>${achievement}</strong>`;
  $("resultText").innerHTML = `
    <span class="result-summary">${copy.body}</span>
    <span class="result-report">
      <span><b>${state.lang === "en" ? "Best streak" : "最高连对"}</b>${state.bestStreak}</span>
      <span><b>${state.lang === "en" ? "System rating" : "系统评级"}</b>${copy.grade}</span>
      <span><b>${state.lang === "en" ? "Mode" : "测试模式"}</b>${state.finalMode ? (state.lang === "en" ? "Final" : "最终测试") : `${state.roundSize}`}</span>
    </span>
  `;
  speak(state.lang === "en" ? `Finished. ${state.score} correct. ${rate} percent. System rating ${copy.grade}.` : `本轮结束，答对 ${state.score} 道，正确率 ${rate}%。系统评级 ${copy.grade}。${copy.title}`);
}

